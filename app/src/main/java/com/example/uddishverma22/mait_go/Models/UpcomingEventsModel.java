public class UpcomingEventsModel{

    public String eventName;
    public String organiser;
    public String eventDate;
    public String regisFee;
    public String imageUrl;
    public String organiserNumber;
    public String organiserEmail;
    public String society;

    public String getEventName() {
        return eventName;
    }

    public void setEventName(String eventName) {
        this.eventName = eventName;
    }

    public String getOrganiser() {
        return organiser;
    }

    public void setOrganiser(String organiser) {
        this.organiser = organiser;
    }

    public String getEventDate() {
        return eventDate;
    }

    public void setEventDate(String eventDate) {
        this.eventDate = eventDate;
    }

    public String getRegisFee() {
        return regisFee;
    }

    public void setRegisFee(String regisFee) {
        this.regisFee = regisFee;
    }

    public String getImageUrl() {
        return imageUrl;
    }

    public void setImageUrl(String imageUrl) {
        this.imageUrl = imageUrl;
    }

    public String getOrganiserNumber() {
        return organiserNumber;
    }

    public void setOrganiserNumber(String organiserNumber) {
        this.organiserNumber = organiserNumber;
    }

    public String getOrganiserEmail() {
        return organiserEmail;
    }

    public void setOrganiserEmail(String organiserEmail) {
        this.organiserEmail = organiserEmail;
    }

    public String getSociety() {
        return society;
    }

    public void setSociety(String society) {
        this.society = society;
    }
}
